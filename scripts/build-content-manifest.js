/**
 * build-content-manifest.js
 * =========================
 * Genera src/data/static_content.ts embebiendo todos los JSON de ejercicios
 * para que funcionen en Vercel (serverless sin acceso a node:fs).
 *
 * Se ejecuta en prebuild / postinstall.
 */

const fs = require("node:fs");
const path = require("node:path");

const EXERCISE_ENGINE_DIR = path.resolve("docs/04_exercise_engine");
const CONTENT_UNITS_DIR = path.resolve("content/lengua/reading_units");
const CONTENT_EXERCISES_DIR = path.resolve("content/lengua/exercises");
const OUTPUT_FILE = path.resolve("src/data/static_content.ts");

const EXCLUDED_FILES = [
  "lengua_skill_relationships.json",
  "lengua_mastery_map.json",
  "lengua_content_index.json",
];

function readJson(filePath) {
  try {
    return JSON.parse(fs.readFileSync(filePath, "utf8"));
  } catch (e) {
    console.error(`Error reading ${filePath}:`, e.message);
    return null;
  }
}

function listJsonFiles(dir, prefix = "lengua_", exclude = []) {
  if (!fs.existsSync(dir)) return [];
  return fs
    .readdirSync(dir)
    .filter((f) => f.endsWith(".json"))
    .filter((f) => (prefix ? f.startsWith(prefix) : true))
    .filter((f) => !exclude.includes(f))
    .filter((f) => !f.startsWith("lengua_textgroup_"))
    .sort();
}

function generate() {
  // 1. Core metadata files
  const relationships = readJson(path.join(EXERCISE_ENGINE_DIR, "lengua_skill_relationships.json"));
  const masteryMap = readJson(path.join(EXERCISE_ENGINE_DIR, "lengua_mastery_map.json"));
  const contentIndex = readJson(path.join(EXERCISE_ENGINE_DIR, "lengua_content_index.json"));

  // 2. Exercise engine files
  const exerciseFiles = listJsonFiles(EXERCISE_ENGINE_DIR, "lengua_", EXCLUDED_FILES);
  const exerciseEngineData = {};
  for (const file of exerciseFiles) {
    exerciseEngineData[file] = readJson(path.join(EXERCISE_ENGINE_DIR, file));
  }

  // 3. Content reading units
  const readingUnitFiles = fs.existsSync(CONTENT_UNITS_DIR)
    ? fs.readdirSync(CONTENT_UNITS_DIR).filter((f) => f.endsWith(".json")).sort()
    : [];
  const readingUnitsData = {};
  for (const file of readingUnitFiles) {
    readingUnitsData[file] = readJson(path.join(CONTENT_UNITS_DIR, file));
  }

  // 4. Content exercises
  const contentExerciseFiles = fs.existsSync(CONTENT_EXERCISES_DIR)
    ? fs.readdirSync(CONTENT_EXERCISES_DIR).filter((f) => f.endsWith(".json")).sort()
    : [];
  const contentExercisesData = {};
  for (const file of contentExerciseFiles) {
    contentExercisesData[file] = readJson(path.join(CONTENT_EXERCISES_DIR, file));
  }

  const lines = [];
  lines.push(`// AUTO-GENERATED by scripts/build-content-manifest.js`);
  lines.push(`// Do not edit manually. Run 'node scripts/build-content-manifest.js' to regenerate.`);
  lines.push(`// Static data — can be imported in both server and client components`);
  lines.push(``);

  // Export relationships
  lines.push(`export const staticRelationships = ${JSON.stringify(relationships)} as { relationships?: unknown[] };`);
  lines.push(``);

  // Export mastery map
  lines.push(`export const staticMasteryMap = ${JSON.stringify(masteryMap)} as { mastery_map?: unknown[] };`);
  lines.push(``);

  // Export content index
  lines.push(`export const staticContentIndex = ${JSON.stringify(contentIndex)} as Record<string, unknown>;`);
  lines.push(``);

  // Export exercise engine data
  lines.push(`export const staticExerciseEngineFiles: Record<string, unknown> = {`);
  for (const file of Object.keys(exerciseEngineData)) {
    const varName = file.replace(/\.json$/, "").replace(/[^a-zA-Z0-9_]/g, "_");
    lines.push(`  "${file}": ${JSON.stringify(exerciseEngineData[file])},`);
  }
  lines.push(`};`);
  lines.push(``);

  // Export reading units
  lines.push(`export const staticReadingUnits: Record<string, unknown> = {`);
  for (const file of Object.keys(readingUnitsData)) {
    lines.push(`  "${file}": ${JSON.stringify(readingUnitsData[file])},`);
  }
  lines.push(`};`);
  lines.push(``);

  // Export content exercises
  lines.push(`export const staticContentExercises: Record<string, unknown> = {`);
  for (const file of Object.keys(contentExercisesData)) {
    lines.push(`  "${file}": ${JSON.stringify(contentExercisesData[file])},`);
  }
  lines.push(`};`);
  lines.push(``);

  // Helper arrays
  lines.push(`export const staticExerciseEngineFileNames = ${JSON.stringify(Object.keys(exerciseEngineData))};`);
  lines.push(`export const staticReadingUnitFileNames = ${JSON.stringify(Object.keys(readingUnitsData))};`);
  lines.push(`export const staticContentExerciseFileNames = ${JSON.stringify(Object.keys(contentExercisesData))};`);
  lines.push(``);

  fs.writeFileSync(OUTPUT_FILE, lines.join("\n") + "\n", "utf8");
  console.log(`Generated ${OUTPUT_FILE}`);
  console.log(`  - ${Object.keys(exerciseEngineData).length} exercise engine files`);
  console.log(`  - ${Object.keys(readingUnitsData).length} reading units`);
  console.log(`  - ${Object.keys(contentExercisesData).length} content exercises`);
}

generate();
